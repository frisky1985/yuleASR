#define PROGRAM_NAME "dds-config"
#define PROGRAM_VERSION "1.0.0"

/* Command types */
typedef enum {
    CMD_NONE = 0,
    CMD_INIT,
    CMD_VALIDATE,
    CMD_GENERATE,
    CMD_APPLY,
    CMD_EXPORT,
    CMD_LIST,
    CMD_INTERACTIVE,
    CMD_HELP,
    CMD_VERSION
} command_t;

/* Command options */
typedef struct {
    command_t command;
    char *input_file;
    char *output_file;
    char *output_dir;
    dds_config_format_t format;
    dds_validation_options_t validation_opts;
    int domain_id;
    bool list_topics;
    bool list_qos;
    bool list_domains;
    bool list_participants;
    bool verbose;
    bool quiet;
} cli_options_t;

/* Function prototypes */
static void print_usage(const char *program);
static void print_version(void);
static void print_help(void);
static int parse_args(int argc, char *argv[], cli_options_t *opts);
static int cmd_init(const cli_options_t *opts);
static int cmd_validate(const cli_options_t *opts);
static int cmd_generate(const cli_options_t *opts);
static int cmd_list(const cli_options_t *opts);
static int cmd_export(const cli_options_t *opts);
static dds_configuration_t* create_default_config(void);
static int write_config_file(const dds_configuration_t *config,
                              const char *filename,
                              dds_config_format_t format);

